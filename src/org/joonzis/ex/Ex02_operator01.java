package org.joonzis.ex;

public class Ex02_operator01 {

	public static void main(String[] args) {
		
		System.out.println(5/3);
		System.out.println(5%3);
		
		int num = 81;
		int front = num / 10;
		int back = num % 10;
		
		System.out.println(front + "/" + back);
	

		int num2 = 3025;
		int front2 = num2 / 100;
		int back2 = num2 % 100;
		
		System.out.println(front2 + "/" + back2);
		
		
		
	}
}
